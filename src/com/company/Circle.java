package com.company;

public class Circle {
    private int r;
    public int getR()
    {
        return r;
    }
    public void setR(int r)
    {
        this.r = r;
    }

    public double Lenth(int r)
    {
        return (2 * 3.14 * r);
    }

    public double Square(int r)
    {
        return (3.14 * r * r);
    }




}